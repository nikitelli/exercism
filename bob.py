def response(hey_bob):
    if hey_bob == "How are you?":
        print("Sure")
    elif hey_bob == "YELL AT HIM":
        print("Whoa, chill out!")
    elif hey_bob == "HEY WHATS UP?":
        print("Calm down, I know what I'm doing!")
    elif hey_bob == "  ":
        print("Fine. Be that way!")
    else:
        print("Whatever")

response("How are you?")